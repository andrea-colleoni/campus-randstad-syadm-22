# commenti: 
'''
- python è case sensitive
- le stringhe (pezzi di testo) sono delimitate dai caratteri ' oppure "
- i comandi che accettano argomenti si chiamano funzioni
- le funzioni per essere chiamate (eseguite) hanno necessariamente bisogno di parentesi tonde
'''

print('ciao mondo')
# print è una funzione
# 'ciao mondo' è un argomento della funzione di tipo stringa

print # cito la funzione print
print() # eseguo (chiamo) la funzione

# variabili e tipi di dato
x = '5'
print('il valore di x è ' + x)
a = 10
b = 20
print(a + b)
# gli operatori cambiano il loro comportamento in base al tipo di dato a cui si applicano (overloading)

print(f'la somma di a e b è {a + b}')